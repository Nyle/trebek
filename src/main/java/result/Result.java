package trebek.result;

import java.util.ArrayList;

import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBAttribute;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBHashKey;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBTable;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBAutoGeneratedKey;

@DynamoDBTable(tableName="QuizResults")
public class Result {
        private String id;
        private Boolean isDone;
        private String customerID;
        private Integer question;
        private ArrayList<Boolean> results;
        
        @DynamoDBAutoGeneratedKey()
        @DynamoDBHashKey(attributeName="ID")
        public String getId() { return id; }
        public void setId(String id) { this.id = id; }

        @DynamoDBAttribute(attributeName="IsDone")
        public Boolean getIsDone() { return isDone; }
        public void setIsDone(Boolean isDone) { this.isDone = isDone; }
        
        @DynamoDBAttribute(attributeName="CustomerID")
        public String getCustomerID() { return customerID; }    
        public void setCustomerID(String customerID) { this.customerID = customerID; }

        @DynamoDBAttribute(attributeName="Results")
        public ArrayList<Boolean> getResults() { return results; }
        public void setResults(ArrayList<Boolean> results) {
                this.results = results;
        }
        
        @DynamoDBAttribute(attributeName="Question")
        public Integer getQuestion() { return question; }
        public void setQuestion(Integer question) {
                this.question = question;
        }        
}
